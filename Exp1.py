# Operators
x = 10
y = 3

print("\nArithmetic Operators:")
print("Addition:", x + y)
print("Modulus:", x % y)
print("Power:", x ** y)

print("\nRelational Operators:")
print("x > y : ", x > y)
print("x == y : ", x == y)
print("x >= y ",x >= y)

print("\nLogical Operators:")
print("True and False : ",True and False)
print("True or False : ",True and False)

print("\nAssignment Operator:")
x += 2
print("x += 2 : ",x)
x -= 3
print("x -= 3 : ",x)
x *= 4
print("x *= 4 : ",x)
